void draw_mesh(Block *block, vec3 position, Shader *shader)
{
    mat4 model;
    glm_mat4_identity(model);
    glm_translate(model, position);
    
    set_mat4(shader, "model", model);
    
    glDrawArrays(GL_TRIANGLES, 0, 36);
}